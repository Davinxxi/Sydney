import numpy as np



def spherical_to_cartesian(azimuth, elevation, r):
    azimuth = np.deg2rad(azimuth)
    elevation = np.deg2rad(elevation)
    
    x = r * np.cos(elevation) * np.cos(azimuth)
    y = r * np.cos(elevation) * np.sin(azimuth)
    z = r * np.sin(elevation)
    
    return x, y, z

def tetrahedral_mic_pos():
    m1 = spherical_to_cartesian(45, 35, 4.2)
    m2 = spherical_to_cartesian(-45, -35, 4.2)
    m3 = spherical_to_cartesian(135, -35, 4.2)
    m4 = spherical_to_cartesian(-135, 35, 4.2)
    
    pos_4 = np.array([m1, m2, m3, m4])
    
    return {4:pos_4/100}


